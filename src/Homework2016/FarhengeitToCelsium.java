public class FarhengeitToCelsium {

        public double FahrenheitToCelsium(double ft){
            return (ft-32)*5/9;
        }

    public class CelsiumToFarhengeit {
        public double CelsiumToFarhengeit (double cm){
            return (cm * 5 / 9) + 32;
        }
    }

}
